<div class="info-section">
    <h2>Panel de Administración Avanzada</h2>
    <p>
        Como administrador de NuevasTecnologias, tienes el control total del sistema de mesa de ayuda.
        Tu función es estratégica y fundamental para mantener la operatividad, eficiencia y calidad del
        servicio que ofrecemos a nuestros usuarios. Desde este panel puedes supervisar, gestionar y
        optimizar todo el flujo de trabajo del departamento de soporte técnico.
    </p>
    <p>
        <strong>Gestión de Usuarios:</strong> Puedes crear nuevos técnicos, promover usuarios a técnicos,
        degradar técnicos a usuarios regulares, y mantener un control completo sobre los roles del sistema.
        Esta funcionalidad te permite adaptar el equipo según las necesidades operativas y el crecimiento
        de la empresa.
    </p>
    <p>
        <strong>Administración de Tickets:</strong> Con los filtros avanzados implementados, puedes:
    </p>
    <ul>
        <li><strong>Monitorear el estado general:</strong> Visualiza tickets por estado, prioridad y asignación.</li>
        <li><strong>Asignar recursos estratégicamente:</strong> Distribuye la carga de trabajo entre técnicos de manera eficiente.</li>
        <li><strong>Identificar cuellos de botella:</strong> Detecta tickets sin asignar o de alta prioridad que requieren atención inmediata.</li>
        <li><strong>Supervisar rendimiento:</strong> Revisa qué técnicos manejan más casos y cómo se resuelven.</li>
        <li><strong>Usar atajos de teclado:</strong> Ctrl+1 (sin asignar), Ctrl+2 (urgentes), Ctrl+0 (limpiar filtros).</li>
    </ul>
    <p>
        <strong>Métricas Clave:</strong> El dashboard te proporciona estadísticas en tiempo real sobre el total
        de tickets, casos pendientes y tickets sin asignar. Esta información te permite tomar decisiones
        informadas sobre la asignación de recursos y la planificación estratégica del departamento.
    </p>
    <p>
        Tu liderazgo y gestión eficiente garantizan que NuevasTecnologias mantenga su reputación de
        excelencia en el servicio al cliente. ¡El éxito del equipo depende de tu visión estratégica!
    </p>
</div>
